// 文件名: scripts/RULE_SPACE_SEC_A.groovy (保密室专属规则)

// 1. 统一全局变量名，全部去掉 def！
拿东西的人 = action.personName
运行方向 = action.direction
// 增加一道防线：防止 action.tags 为空导致后续调用 .size() 报错
物品清单 = action.tags ?: []

允许放行 = true
开门提示信息 = ""

// --- 动作 A：往里进 (IN) ---
if (运行方向 == "IN") {
    if (物品清单.size() > 0) {
        for (物品 in 物品清单) {
            // 🔥 核心修改：只对有 tagId 的追踪物品进行核销
            if (物品.tagId != null && 物品.tagId.toString().trim() != "") {
                tracker.arrive(物品.tagId, "SPACE_SEC_A")
            }
            // 正常物品不满足上述 if 条件，会自动跳过，不触发 tracker
        }
        开门提示信息 = "带物品入库保密室成功"
    } else {
        // 补充逻辑：完全空手进入保密室
        开门提示信息 = "人员空手进入保密室"
    }
}

// --- 动作 B：往外走 (OUT)，严苛的防盗锁门逻辑 ---
if (运行方向 == "OUT") {
    if (物品清单.size() > 0) {
        // 带有物品出门，触发特权判断：除非是保密员王芳在操作，否则谁都不行
        if (拿东西的人 != "保密员-王芳") {
            alert.send("最高级别入侵警报", "【${拿东西的人}】正试图从二楼核心保密室带出物理载体！")
            允许放行 = false
        } else {
            alert.send("保密室通知", "保密员王芳正在执行物资调拨。")
            开门提示信息 = "保密室物资调拨权限校验通过"
        }
    } else {
        // 补充逻辑：空手离开保密室，不需要特权
        开门提示信息 = "人员空手离开保密室"
    }
}

// --- 最终闸机控制 (统一收口执行) ---
if (允许放行 == false) {
    door.block("保密室防盗门已死锁！并联动安保中心！")
} else {
    // 无论是进还是出，只要允许放行，就在这里统一开门，并输出对应的提示语
    door.open(开门提示信息)
}